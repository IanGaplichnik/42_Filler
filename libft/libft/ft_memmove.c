#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t	i;

	if (!dst && !src)
		return (NULL);
	i = 0;
	if (src > dst)
	{
		while (i < len)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i++;
		}
	}
	else
	{
		i = 1;
		while (i < len + 1)
		{
			((unsigned char *)dst)[len - i] = ((unsigned char *)src)[len - i];
			i++;
		}
	}
	return (dst);
}
